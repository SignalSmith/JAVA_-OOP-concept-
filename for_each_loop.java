
public class for_each_loop
 {
     
      public static void main(String args[])
      {
          
          int  arr[] = new int [3]; 

          arr[0]=10;
          arr[1]=9;
          arr[2]=12;

           for(int n : arr )   // syntax of for each loop 
           {
            System.out.print(n + " ");
           }
      }
    
}
